<template>
    <input type="text" v-model="message" @keyup="filter()" />
</template>

<script>
import * as actionsList from '@/store/actions-list'

export default {
    name: 'Message',
    data: () => {
        return {
            message: ''
        }
    },
    methods: {
        filter () {
            this.$store.dispatch(actionsList.FILTER_BY_MESSAGE, this.message)
        }
    }
}
</script>

